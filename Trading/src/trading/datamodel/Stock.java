package trading.datamodel;;

/**
 * @author Kwang
 * Models an equity that can be traded on the stock market.
 */
public class Stock extends FinancialSecurity {
	
	/**
	* This constructor only sets the two fields needed for an instance.
	* It should be used as a means to collect data from the database using it's stored
	* Id
	*/
	public Stock(int i, String symbol)
	{
		setTicker(symbol);
		Id = i;
	}

}
